      <h2>Good Ending Walkthrough</h2>
      <h3>Clearing in a Forest</h3>
      <p>Look at the hole and take the stone inside.</p>
      <h3>Forest Bridge, South Bank</h3>
      <p>
        Look at the bridge to see the rod. Throw the stone (throw stone), which
        distracts the pirahna. Take the rod.
      </p>
      <h3>A Quiet Fork in the Forest</h3>
      <p>Look at the wood and take the nail you see.</p>
      <h3>Inside the Creepy Hut</h3>
      <p>
        Greet the woman and tell her you're not a threat ("no"). Then ask her
        about the cure ("ask rhode about cure"). She mentions ingredients. Ask
        her about them. Ask rhode about puffball too. Then ask her about
        scissors. Tell her "yes", you want them. Take the scissors.
      </p>
      <h3>A Pool of Standing Water</h3>
      <p>Use the scissors on a puffball. (cure ingredient 1)</p>
      <h3>Mosquito Spawning Pool</h3>
      <p>Take a mosquito egg. (cure ingredient 2)</p>
      <h3>White Sand Beaches, East</h3>
      <p>Look at the boat to see the sledgehammer. Take the sledgehammer.</p>
      <h3>Ruined Hall</h3>
      <p>
        There's a device on the wall that opens the way to the east but it's
        missing a handle. Use the rod on the device. Then use the device itself.
        Take the rod once the way is open.
      </p>
      <h3>Ruined Square, South</h3>
      <p>The south wall is weak. Use the sledgehammer on the wall.</p>
      <h3>Ruined House</h3>
      <p>Open the chest. Take the flashlight.</p>
      <h3>Ruined Square, North</h3>
      <p>Use the flashlight to turn it on. Open the hatch. Go down.</p>
      <h3>Inside the Hatch</h3>
      <p>
        Take the rune. It's the etavoner rune. You need to dunk it in the forest
        well to activate it.
      </p>
      <h3>Ruined Lab</h3>
      <p>Look at the poster. Look at the vials. Take the potion.</p>
      <h3>An Old Well in the Forest</h3>
      <p>
        Put the etavoner rune in the pail. Drop the pail. Use the crank. Take
        the rune. With your flashlight on, go east.
      </p>
      <h3>Cave of Bones</h3>
      <p>
        Look at the hornet. Look at the sac. Take the sac. This allows entry
        into the hornet hive later.
      </p>
      <h3>A Rocky Cliff Peninsula, High Above the Ocean</h3>
      <p>
        Incant etavoner to repair the bridge. Go south to the lighthouse and
        enter it.
      </p>
      <h3>Lighthouse, First Floor</h3>
      <p>Look at the bed, then the pillow. Take the key. Go up.</p>
      <h3>Lighthouse, Below the Light</h3>
      <p>Use the key on the padlock. Go up.</p>
      <h3>Lighthouse, Light</h3>
      <p>
        Give the potion to the woman (her name's Ionyne). She drinks the potion
        and gives you back an empty bottle. She makes you a bird friend.
      </p>
      <h3>Gull Nesting Grounds</h3>
      <p>
        Now that you're a bird friend, take the rainbow shell from the nest.
        (cure ingredient 3)
      </p>
      <h3>An Old Well in the Forest</h3>
      <p>
        Return to the old well. You should still have the pail full of water
        sitting on the side of the well. Use the empty bottle on the water to
        take some.
      </p>
      <h3>Ruined Temple</h3>
      <p>
        Use the rod on the device. Use the device. You can leave the rod. You
        won't need it again. Go down.
      </p>
      <h3>Underground Chasm</h3>
      <p>Incant etavoner to repair the bridge. Go south.</p>
      <h3>Silent Chamber</h3>
      <p>Put the water bottle onto the scale. The passage west opens.</p>
      <h3>Small Mountain Lake</h3>
      <p>
        Look at the plants. Take the white-leafed plant. (cure ingredient 4)
      </p>
      <h3>Campsite at the Cliffside</h3>
      <p>
        Look at the pouch. Take the rune. To activate the rune, you must give it
        to the hornet queen. Return all the way to the hornet's nest area in the
        forest.
      </p>
      <h3>The Hive Entrance</h3>
      <p>
        Go down. You have the pheromone sac from the Cave of Bones so you'll be
        ignored.
      </p>
      <h3>Hive, Queen's Chamber</h3>
      <p>Give the eltnamsid rune to the queen.</p>
      <h3>The Bat Cave</h3>
      <p>Incant eltnamsid to destroy the wall. Go through.</p>
      <h3>Hidden Cache</h3>
      <p>Take the silver box.</p>
      <h3>Inside the Creep Hut</h3>
      <p>
        Give the silver box to Rhode. Take the map. Return all the way to the
        mountain labyrinth.
      </p>
      <h3>Labyrinth, South Gate</h3>
      <p>
        Exit to the east or west in either the Labyrinth, South Gate; Labyrinth,
        Central Plaza; or Labyrinth, North Gate.
      </p>
      <h3>A Labyrinthine Passage</h3>
      <p>
        You should look at your map. It will tell you which direction to go.
        Look at it, then go that direction ONCE. Repeat 10 total times. If
        you're lazy and don't want to do that, just follow these directions: n,
        e, n, s, s, w, s, e, n, e. If you do it wrong, you will be returned to
        one of the central Labyrinth rooms.
      </p>
      <h3>Heart of the Labyrinth</h3>
      <p>
        Take the rune. To activate the lievnu rune, you must use it on the
        lighthouse's light. When you take the rune, Rhode will also finish her
        study of the silver box.
      </p>
      <h3>Lighthouse, Light</h3>
      <p>Use the lievnu rune on the light.</p>
      <h3>Inside the Creepy Hut</h3>
      <p>
        This is your last visit to Rhode. She gives you back the silver box.
        Incant etavoner to fix it. Incant lievnu to unlock its power. Give rhode
        the rainbow shell, the puffball, the mosquito egg, and the root. She
        will give you the cure.
      </p>
      <h3>Outside a Log Cabin in the Forest</h3>
      <p>Give the man the nail. This is Arden. Head west into his cabin.</p>
      <h3>Cabin, Interior</h3>
      <p>
        Give the cure to Edra (his sleeping brother). He wakes up and you learn
        to incant rhocna.
      </p>
      <h3>Toppled Tree, Growing Mushrooms</h3>
      <p>
        Incant rhocna to freeze the mushrooms in place. You need to touch the
        colored mushrooms in order. Each color represents a character in the
        game. You learn the order by finding Ophelia's journal in Long Abandoned
        Campsite in the tent. You can give the journal to Rhode to repair it and
        make it easier to read.
      </p>
      <p>
        The character order is Edra (blue eyes), Mine! (Ophelia's yellow hair),
        Merek (red cape), Rhode (purple light and eyes), Arden (orange shirt),
        Meredith (green sneakers), Stanley (indigo suit). Touch the mushrooms in
        this order: blue, yellow, red, purple, orange, green, indigo.
      </p>
      <p>
        You then experience memories of each character in the game and learn to
        incant hsinimid, partne, and etalosi.
      </p>
      <h3>The Black Summit</h3>
      <p>
        Incant lievnu to reveal It. Incant hsinimid to weaken it. Open the
        silver box. Incant partne to trap it in the box. Then incant etalosi.
      </p>
